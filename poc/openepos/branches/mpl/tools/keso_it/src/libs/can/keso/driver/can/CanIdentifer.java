package keso.driver.can;

/**
 *
 * @author Ralf Ellner
 */
public class CanIdentifer {
    
    private int identifer;
    
    public CanIdentifer(int identifer) {
        setCan2Identifer(identifer);
    }
    
    public int getCan2Identifer() {
        return this.identifer;
    }
    
    public short getCan1Identifer() {
        return (short) this.identifer;
    }
    
    public void setCan2Identifer(int identifer) {
        this.identifer = identifer;
    }
    
    public void setCan1Identifer(short identifer) {
        this.identifer = identifer;
    }
    
}
