package josek;

public class PrimitiveIntAttribute extends PrimitiveAttribute {
	int value;

	public PrimitiveIntAttribute(Attribute parent, String lValue, int value) {
		super(parent, lValue);
		this.value = value;
	}

	public String toString() {
		StringBuffer s = new StringBuffer();
		s.append("Integer '");
		s.append(lValue);
		s.append("' = ");
		s.append(value);
		return s.toString();
	}
}
