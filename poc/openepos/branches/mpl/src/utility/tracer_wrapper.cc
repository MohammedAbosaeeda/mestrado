extern "C" {
    #include <utility/tracer_c.h>
}

#include <utility/tracer.hh>

void tracer_trace(const char * message) {
    System::Tracer::trace(message);
}

void tracer_trace_i(const char * message, int value) {
	System::Tracer::trace_i(message, value);
}

void tracer_trace_s_ulong_s(const char* m1, unsigned long l1, const char* m2)
{
	System::Tracer::trace_s_ulong_s(m1, l1, m2);
}
